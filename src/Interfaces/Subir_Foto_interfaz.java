    private void subirFotoServidor(File foto) {
        
        try {

            //1 Convierto la foto a bytes[]
            byte[] imagenEnBytes = Files.readAllBytes(foto.toPath());

            //2 Crea una peticion Socket para enviar la imagen y sus flujos
            final String DIRECCION_IP = "192.168.0.11";
            final int PUERTO_ENVIAR = 9004;
            Socket conexionSocketEnviarImagen = new Socket(DIRECCION_IP, PUERTO_ENVIAR);
            BufferedOutputStream outputSocket = new BufferedOutputStream(conexionSocketEnviarImagen.getOutputStream());
            BufferedReader inputSocket = new BufferedReader(new InputStreamReader(conexionSocketEnviarImagen.getInputStream()));

            //3 Envia la imagen
            outputSocket.write(imagenEnBytes, 0, imagenEnBytes.length);
            outputSocket.flush();
            //4 Recibir la ruta de la imagen o el error
            String theInput;
            theInput = inputSocket.readLine();
            
            if (theInput.contains("S22-IMAGEN_SUBIDA")) {
                String rutaImagenSubida = Utilidades.obtenerParametro(theInput, 1);
                añadirRutaImagenSubidaEnPanel(rutaImagenSubida);
                this.dispose();
            } else {
                error_lb.setText("Error del servidor, intentelo mas tarde.");
            }
            outputSocket.close();
            inputSocket.close();
            conexionSocketEnviarImagen.close();

        } catch (IOException ex) {
            Logger.getLogger(Subir_Foto_interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void añadirRutaImagenSubidaEnPanel(String rutaImagenSubida) {

        switch (nEjercicio) {
            case 1:
                panelPadreCliente.añadirRutaImagenSubida(rutaImagenSubida);
                break;
            case 2:
                panelPadreClase.añadirRutaImagenSubida(rutaImagenSubida);
                break;
            case 3:
                panelPadreEjercicio.añadirRutaImagenSubida(rutaImagenSubida);
                break;
        }
    }
