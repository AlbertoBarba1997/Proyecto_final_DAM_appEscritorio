    private void selectorRuta_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectorRuta_btActionPerformed
        //Crear objeto selector de archivos
        JFileChooser fileChooser = new JFileChooser();

        //Añadirle un filtro para que solo muestre el tipo de archivos que nos interesa, en este caso de fotos png y jpg
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("imagenes", "png", "jpg");
        fileChooser.setFileFilter(filtro);

        //Mostrar la ventana del seleccionador y recoger la opcion seleccionada (si ha "aceptado", "cancelado"...)
        int seleccion = fileChooser.showOpenDialog(this);

        //Si finalmente la opcion seleccionada es "aceptar" pues recogerá el File que se ha seleecionado, obtendra su ruta y la cambiará en el EditText de esta.
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File foto = fileChooser.getSelectedFile();
            ruta_tf.setText(foto.getAbsolutePath());
        }
    }//GEN-LAST:event_selectorRuta_btActionPerformed

    private void subir_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subir_btActionPerformed
        //Comprobará la ruta del TextField (Que habra sido introducida manualmente o con el FileChooser) y la subirá al servidor
        File foto = new File(ruta_tf.getText());
        //Comprobar si el archivo existe
        if (foto.exists()) {
            //Comprobar si es una foto
            String mimetype = new MimetypesFileTypeMap().getContentType(foto);
            String type = mimetype.split("/")[0];
            if (type.equals("image")) {
                subirFotoServidor(foto);
            } else {
                error_lb.setText("El archivo indicado no es una imagen (png o jpg)");
            }

        } else {
            error_lb.setText("El archivo indicado no existe. Vuelva a indicar la ruta.");
        }
    }//GEN-LAST:event_subir_btActionPerformed

