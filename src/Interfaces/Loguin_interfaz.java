    public void log(){
        String dni = dni_tf.getText();
        String clave = clave_tf.getText();

        int resultadoLog = logIn(dni, clave);

        switch (resultadoLog) {
            case -1:
                error_lb.setText("Credenciales incorrectas. Intentalo de nuevo");
                break;
            case 1:
                System.out.println("Log admin:"+nombreUsuario);
                new Menu_admin_interfaz(inputSocket,outputSocket, this.nombreUsuario).setVisible(true);
                this.dispose();
                break;
            case 2:
                System.out.println("Log entrenador");
                new Menu_coach_interfaz(inputSocket,outputSocket, nombreUsuario).setVisible(true);
                this.dispose();
                
                break;
        }
    }
    private int logIn(String dni, String clave) {
        int resultado=-1;
        try {
            outputSocket.println("C4-LOG_TRABAJADOR:" + dni + "," + clave);
            outputSocket.flush();
            String mensajeEntrada = inputSocket.readLine();
            
            if(mensajeEntrada.contains("S2-LOG_INCORRECTO")){
                resultado=-1;
            }else if(mensajeEntrada.contains("S10-LOG_CORRECTO_TR")){
                String rol=Utilidades.obtenerParametro(mensajeEntrada, 1);
                String nombre=Utilidades.obtenerParametro(mensajeEntrada, 2);
                String apellidos=Utilidades.obtenerParametro(mensajeEntrada, 3);
                this.nombreUsuario=nombre+" "+apellidos;
                resultado=Integer.parseInt(rol);
                
                
            }
            
            
            
        } catch (IOException ioe) {
            
        }

        return resultado;
    }
    
    
    

    private void iniciarConexion() {
        try {
            //Replace below IP with the IP of that device in which server socket open.
            //If you change port then change the port number in the server side code also.
            conexionSocket = new Socket("192.168.0.11", 9002);
            outputSocket = new PrintWriter(conexionSocket.getOutputStream());
            inputSocket = new BufferedReader(new InputStreamReader(conexionSocket.getInputStream()));
            

            
        } catch (IOException e) {
            e.printStackTrace();
            error_lb.setText("Error de conexion con servidor Â¿Volver a intentar?");
        }
    }
    
    
